package com.dzinesunlimited.resto.utils.helpers.pojos.frontend;

public class DishesData {

    private String menuID;
    private String menuCategoryID;
    private String menuName;
    private String menuDescription;
    private String menuPrice;
    private String menuServes;
    private String menuType;
    private byte[] menuImage;

    public String getMenuID() {
        return menuID;
    }

    public void setMenuID(String menuID) {
        this.menuID = menuID;
    }

    public String getMenuCategoryID() {
        return menuCategoryID;
    }

    public void setMenuCategoryID(String menuCategoryID) {
        this.menuCategoryID = menuCategoryID;
    }

    public String getMenuName() {
        return menuName;
    }

    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    public String getMenuDescription() {
        return menuDescription;
    }

    public void setMenuDescription(String menuDescription) {
        this.menuDescription = menuDescription;
    }

    public String getMenuPrice() {
        return menuPrice;
    }

    public void setMenuPrice(String menuPrice) {
        this.menuPrice = menuPrice;
    }

    public String getMenuServes() {
        return menuServes;
    }

    public void setMenuServes(String menuServes) {
        this.menuServes = menuServes;
    }

    public String getMenuType() {
        return menuType;
    }

    public void setMenuType(String menuType) {
        this.menuType = menuType;
    }

    public byte[] getMenuImage() {
        return menuImage;
    }

    public void setMenuImage(byte[] menuImage) {
        this.menuImage = menuImage;
    }
}